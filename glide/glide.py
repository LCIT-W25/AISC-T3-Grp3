def get_dummy_text_embedding(text, device):
    # Replace this with your actual CLIP or other text encoder inference
    # Here we simply return a zero tensor (of shape [1, 512])
    return torch.zeros(1, 512, device=device)

def generate_image_from_caption(input_caption, model_path, device="cpu"):
    # Load the model
    model = ConditionalUNet().to(device)
    model.load_state_dict(torch.load(model_path, map_location=device))
    model.eval()

    # Obtain text embedding (here we use a dummy embedding; replace with your own)
    text_embedding = get_dummy_text_embedding(input_caption, device)

    # Start from pure noise
    img_size = 256
    current_image = torch.randn(1, 3, img_size, img_size, device=device)

    # Reverse diffusion process (a simplified deterministic version)
    for i in tqdm(reversed(range(T)), desc="Generating Image"):
        t = torch.full((1,), i, device=device, dtype=torch.long)
        with torch.no_grad():
            predicted_noise = model(current_image, t, text_embedding)
        a_t = extract(alphas_cumprod, t, current_image.shape)
        beta_t = extract(betas, t, current_image.shape)
        current_image = (current_image - (beta_t / torch.sqrt(1 - a_t)) * predicted_noise) / torch.sqrt(1 - beta_t)

    # Denormalize: from [-1, 1] to [0, 1]
    generated_image = current_image.squeeze(0).cpu().detach()
    generated_image = (generated_image * 0.5 + 0.5).clamp(0, 1)
    pil_img = transforms.ToPILImage()(generated_image)
    return pil_img

