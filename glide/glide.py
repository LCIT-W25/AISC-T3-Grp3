# -------------------------------
# Define the simplified conditional UNet and helper functions
# -------------------------------

class SinusoidalTimeEmbedding(nn.Module):
    def __init__(self, dim):
        super().__init__()
        self.dim = dim

    def forward(self, t):
        half_dim = self.dim // 2
        emb = math.log(10000) / (half_dim - 1)
        emb = torch.exp(torch.arange(half_dim, device=t.device) * -emb)
        emb = t.float().unsqueeze(1) * emb.unsqueeze(0)
        emb = torch.cat([torch.sin(emb), torch.cos(emb)], dim=1)
        if self.dim % 2 == 1:  # zero pad if needed
            emb = torch.cat([emb, torch.zeros(t.size(0), 1, device=t.device)], dim=1)
        return emb

class ConditionalUNet(nn.Module):
    def __init__(self, time_emb_dim=128, text_emb_dim=512):
        super().__init__()
        self.time_mlp = nn.Sequential(
            SinusoidalTimeEmbedding(time_emb_dim),
            nn.Linear(time_emb_dim, time_emb_dim),
            nn.ReLU()
        )
        self.down = nn.Sequential(
            nn.Conv2d(3, 64, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.Conv2d(64, 128, kernel_size=4, stride=2, padding=1),  # from 256 -> 128
            nn.ReLU(),
        )
        self.bottleneck = nn.Sequential(
            nn.Conv2d(128, 128, kernel_size=3, padding=1),
            nn.ReLU(),
        )
        self.condition_proj = nn.Linear(time_emb_dim + text_emb_dim, 128)
        self.up = nn.Sequential(
            nn.ConvTranspose2d(128, 64, kernel_size=4, stride=2, padding=1),  # from 128 -> 256
            nn.ReLU(),
            nn.Conv2d(64, 3, kernel_size=3, padding=1)
        )

    def forward(self, x, t, text_embedding):
        t_emb = self.time_mlp(t)  # (B, time_emb_dim)
        cond = torch.cat([t_emb, text_embedding], dim=1)  # (B, time_emb_dim + text_emb_dim)
        cond = self.condition_proj(cond)  # (B, 128)
        cond = cond.unsqueeze(-1).unsqueeze(-1)  # (B, 128, 1, 1)
        x_down = self.down(x)         # (B, 128, H/2, W/2)
        x_bot = self.bottleneck(x_down)
        x_bot = x_bot + cond
        out = self.up(x_bot)
        return out

# -------------------------------
# Diffusion Schedule Helpers
# -------------------------------
T = 1000  # total diffusion steps

def linear_beta_schedule(timesteps):
    beta_start = 0.0001
    beta_end = 0.02
    return torch.linspace(beta_start, beta_end, timesteps)

betas = linear_beta_schedule(T)
alphas = 1.0 - betas
alphas_cumprod = torch.cumprod(alphas, dim=0)

def extract(a, t, x_shape):
    batch_size = t.shape[0]
    out = a.gather(-1, t.cpu()).reshape(batch_size, *((1,) * (len(x_shape) - 1))).to(t.device)
    return out

